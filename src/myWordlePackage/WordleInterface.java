    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String regiUsername = txfUNameRegister.getText();
        String password = pasUPassRegi.getText();
        String confirmPassword= pasUPassConfirmRegi.getText();
        
        if(regiUsername.length()>=5 &&password.length()>=8&&wl.contains3Numbers(password) && wl.contains3Numbers(regiUsername) == true && password.equals(confirmPassword))
        {
            wl.Register(regiUsername, password);
            JOptionPane.showMessageDialog(this, "Registration successful. \nDo Not Forget your username and password as you login\n(It is imperative)");
            frmLogin.setVisible(true);
            frmRegister.setVisible(false);
            System.out.println("true");
        }else if(regiUsername.length()<5 || wl.contains3Numbers(regiUsername)== false)
        {
            JOptionPane.showMessageDialog(this, "Username must be longer than 5 characters and contain  three numbers");
        }else if(wl.containsNumberandSpecial(password)== false || password.length()<8)
        {
            JOptionPane.showMessageDialog(this, "Password should have a special character and number and longer than 8 characters");
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

