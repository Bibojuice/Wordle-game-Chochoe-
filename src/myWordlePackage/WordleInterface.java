    private void btnCheckWordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckWordActionPerformed

        
        String guess = txfWordleInput.getText();
        txaWordleOutput.append(guess + " ");
        
        if (remainingChances > 0){
            JOptionPane.showMessageDialog(this, remainingChances);
            if (guess.length() == 5) {
                String output = "";
                

                    if (word.equalsIgnoreCase(guess)) {
                        JOptionPane.showMessageDialog(this, "Congratulations╰(*°▽°*)╯" + guess + " is correct");
                        this.setVisible(false);
                        remainingChances =0;
                        Score+= 2000;
                        wl.inputScore(txfUsername.getText(), Score);
                        frmScore.setSize(400,450);
                        frmScore.setLocationRelativeTo(null);
                        frmScore.setVisible(true);
                        txfUserNameScore.setText(txfUsername.getText());
                        txfUserNameScore.setEditable(false);
                        txfUserScore.setText(String.valueOf(Score));
                        txfUserScore.setEditable(false);
                    } else{
                        boolean[] matchedPosition = new boolean[word.length()];
                        
                       
                        for(int j=0; j<guess.length();j++)
                        {
                            char letter = guess.charAt(j);
                            if (Character.toUpperCase(word.charAt(j)) == Character.toUpperCase(letter)) {
                                output+= letter + "-green ";
                                Score += 50;
                                matchedPosition[j] = true;
                            } else
                            if(word.toUpperCase().contains(String.valueOf(letter).toUpperCase())) {
                                output += letter +"-grey ";
                                Score+= 25;
                            }else{
                                output+= letter + "-red ";
                                Score+= -25;
                            }
                            
                        }
                    }
                
                txaWordleOutput.append(output + "\n");
                txfWordleInput.setText("");
               
                
                remainingChances--;
            } else {
                JOptionPane.showMessageDialog(this, "You should input a 5 letter word");
            }
           
        }else
        {
            JOptionPane.showMessageDialog(this,"No more chances left! Game Over\nThe correct word is " + word );
            wl.inputScore(txfUsername.getText(), Score);
            frmScore.setSize(400,450);
            frmScore.setLocationRelativeTo(null);
            frmScore.setVisible(true);
            txfUserNameScore.setText(txfUsername.getText());
            txfUserNameScore.setEditable(false);
            txfUserScore.setText(String.valueOf(Score));
            txfUserScore.setEditable(false);
        }
    }//GEN-LAST:event_btnCheckWordActionPerformed

